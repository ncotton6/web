package edu.rit.csci729.model;

/**
 * Simple modal class to hold the information for connectable endpoints.
 * 
 * @author Nathaniel Cotton
 *
 */
public class MappingSource {

	public Object source;
	public String type;
	
	public MappingSource(){}
	public MappingSource(Object source, String type){
		this.source = source;
		this.type = type;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((source == null) ? 0 : source.hashCode());
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		MappingSource other = (MappingSource) obj;
		if (source == null) {
			if (other.source != null)
				return false;
		} else if (!source.equals(other.source))
			return false;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		return true;
	}
	
}
